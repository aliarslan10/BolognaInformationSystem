@section('icerik_yonetim')
Bölüm ile ilgili yetkililerin atanacağı kısımdır. Bölüm başkanı, bölüm AKTS koordinatörü, Bologna Eşgüdüm Komisyonu üyelerinin isimleri girilerek, belirtilen alanlardan sorumlu olacak kişiler belirlenecek.
Bunun dışında yine bu panelden bölüm program çıktıları hem Türkçe hem de İngilizce olarak girilebilecek. Program çıktıları, daha önceden bölümde kurulmuş eğitim komisyonuyla, bölüm başkanlığı ve bölüm başkanı başkanlığında ve ana bilim dalı başkanlarının katılımıyla oluşturulan bir komisyonda hazırlanır. Her bir bölümün mezun ettiği öğrenciye verdiği yeterlilikler, çoğu zamanda paydaşlara danışılarak ortaya çıkarılır. Her bölüm için çıkartılan program çıktıları, bu panelden sisteme tek tek eklenebilecektir. Yeni çıktı eklenebildiği gibi, çıktılar düzenlenebilir veya silinebilir olacak. Program çıktısı girme yetkisi sadece yetki verilen kişiler tarafından yapılabilecek.
