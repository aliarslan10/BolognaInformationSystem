<b> Kurum Hakkında </b>
<br />
İskenderun Teknik Üniversitesi (İSTE) devlet üniversitesidir.

İskenderun Teknik Üniversitesi 2015 yılında Mustafa Kemal Üniversitesinden ayrılarak İskenderun’da kurulmuştur. Üniversitenin ana kampüsü (Merkez) İskenderun şehir merkezinin 2 km uzaklıktadır. 

İskenderun Teknik Üniversitesi; 5 Fakülte, 2 Yüksekokul, 5 Meslek Yüksekokulu, 7 Enstitü, ve Devlet Konservatuvarın' dan oluşmaktadır.
